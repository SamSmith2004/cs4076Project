package ul.Server.Models;

/**
 * The {@code DayOfWeek} enum represents the days of the week.
 * It is used to specify the day on which a lecture is scheduled.
 * 
 * <ul>
 * <li>{@link #MONDAY}</li>
 * <li>{@link #TUESDAY}</li>
 * <li>{@link #WEDNESDAY}</li>
 * <li>{@link #THURSDAY}</li>
 * <li>{@link #FRIDAY}</li>
 * </ul>
 */
public enum DayOfWeek {
    /**
     * Monday
     */
    MONDAY,
    /**
     * Tuesday
     */
    TUESDAY,
    /**
     * Wednesday
     */
    WEDNESDAY,
    /**
     * Thursday
     */
    THURSDAY,
    /**
     * Friday
     */
    FRIDAY
}
